#include <stdio.h>

int main() { 
 int a = 10;
 int b = 5;
 int tong = a+b;
 int tich = a*b;
 int hieu = a-b;
 int thuong = a/b;

  printf("tong: %d + %d =%d", a, b, tong);
  printf("tich: %d * %d =%d", a, b, tich);
  printf("hieu: %d - %d =%d", a, b, hieu);
  printf("thuong: %d \ %d =%d", a, b, thuong);
  
  return 0;
}
